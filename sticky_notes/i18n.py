def normalize_language(language: str) -> str:
    return language if language in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def tr(key: str, language: str = DEFAULT_LANGUAGE, **values: object) -> str:
    selected = normalize_language(language)
    template = _STRINGS[selected].get(key) or _STRINGS[DEFAULT_LANGUAGE].get(key)
    if template is None:
        return key
    return template.format(**values)


def theme_label(theme_key: str, language: str) -> str:
    return tr(f"theme_{theme_key}", language)
